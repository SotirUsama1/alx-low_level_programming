#include "3-calc.h"

/**
 * op_add - add function
 *
 * @a: int 1
 * @b: int 2
 *
 * Return: int
 */

int op_add(int a, int b)
{
	return (a + b);
}

/**
 * op_sub - subtract function
 *
 * @a: int 1
 * @b: int 2
 *
 * Return: int
 */

int op_sub(int a, int b)
{
	return (a - b);
}

/**
 * op_mul - multiply function
 *
 * @a: int 1
 * @b: int 2
 *
 * Return: int
 */

int op_mul(int a, int b)
{
	return (a * b);
}

/**
 * op_div - divsion function
 *
 * @a: int 1
 * @b: int 2
 *
 * Return: int
 */

int op_div(int a, int b)
{
	return (a / b);
}

/**
 * op_mod - modulas function
 *
 * @a: int 1
 * @b: int 2
 *
 * Return: int
 */

int op_mod(int a, int b)
{
	return (a % b);
}
